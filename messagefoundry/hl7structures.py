"""HL7 v2.5.1 message-structure metadata for the Steps-view field picker (ADR 0104 §2.3 P2/P3).

The picker scopes/ranks its segment list by the *message structure* a handler handles, and (P3) badges
each offered field/component path by whether it round-trips on both parser backends. Both need data that
``ide/media/hl7schema.json`` (segment-keyed) does not carry, so this module derives it from **hl7apy**
(the strict-validation catalog, version-pinned to 2.5.1) and the ``messagefoundry generate`` estate:

* **trigger -> structure** (``build_trigger_map``): a ``"CODE^TRIGGER" -> structure`` map. HL7 collapses
  many triggers onto one structure (``ADT^A04``/``A08``/``A13`` are all ``ADT_A01``), and hl7apy keys
  validation by structure — so a naive ``ADT^A08`` lookup misses. The map is the union of (a) the identity
  inversion of every hl7apy ``CODE_TRIGGER`` structure key and (b) the authoritative ADT collapse map
  (:data:`TRIGGER_TO_STRUCTURE`, the single source ``generators/adt.py`` also imports).
* **structure -> segments** (:func:`structure_segments`): a ``kind``-gated walk of hl7apy's ``MESSAGES``
  sequence — recurse into ``GRP`` nodes, collect ``SEG`` node ids, dedup preserving first-seen order.

:func:`to_json` emits the bundled ``ide/media/hl7structures.json`` artifact (regenerated by the
``messagefoundry hl7structures`` CLI). A byte-equal CI test keeps the committed bundle in sync with this
code (the ``requirements.lock`` discipline) so a stale bundle can never ship wrong scope/badges.
"""

