def build_trigger_map() -> dict[str, str]:
    """A ``"CODE^TRIGGER" -> structure id`` map for resolving a handler's declared/inferred message type.

    Union of (a) the **identity inversion** of every hl7apy ``CODE_TRIGGER`` structure key (so ``ORU^R01``
    -> ``ORU_R01``, ``SIU^S12`` -> ``SIU_S12``, … resolve without any per-generator import) and (b) the
    authoritative **ADT collapse** map (:data:`TRIGGER_TO_STRUCTURE`), applied last so a collapsed ADT
    trigger (``ADT^A08`` -> ``ADT_A01``) wins. A trigger with no entry resolves to ``None`` (generic scope)
    — never a *wrong* existing structure."""
    trig: dict[str, str] = {}
    for key in MESSAGES:
        code, sep, trigger = key.partition("_")
        if sep and code and trigger:
            trig[f"{code}^{trigger}"] = key
    for trigger, struct in TRIGGER_TO_STRUCTURE.items():
        trig[f"ADT^{trigger}"] = struct
    return trig


def resolve_structure(code: str | None, trigger: str | None) -> str | None:
    """The structure id for ``CODE^TRIGGER``, or ``None`` if unresolvable (→ generic, unscoped picker).

    A code-only type (no trigger) is unresolvable here (a code maps to many structures); the picker then
    offers the generic segment list. A caller that already knows the structure (an explicit 3-component
    ``"ADT^A01^ADT_A05"`` spec) uses that id directly and never calls this."""
    if not code or not trigger:
        return None
    return build_trigger_map().get(f"{code}^{trigger}")


def structure_segments(struct: str) -> list[str]:
    """The ordered, de-duplicated segment ids of message structure ``struct`` (``[]`` if unknown).

    Walks hl7apy's ``MESSAGES[struct] = ('sequence', (entries...))`` where each entry is
    ``(node_id, child, cardinality, kind)``: recurse into a ``GRP`` child, collect a ``SEG``'s ``node_id``
    (never descend a segment's field sequence), preserving first-seen order (a structure may repeat a
    segment — e.g. ``ADT_A01`` has ``ROL`` several times — which is deduped)."""
    if struct not in MESSAGES:
        return []
    seen: list[str] = []

    def walk(sequence: tuple[object, ...]) -> None:
        entries = sequence[1]
        assert isinstance(entries, tuple)
        for entry in entries:
            node_id, child, _cardinality, kind = entry
            if kind == "GRP":
                walk(child)
            elif kind == "SEG" and node_id not in seen:
                seen.append(node_id)

    walk(MESSAGES[struct])
    return seen


# --- P3: round-trip-verified paths -------------------------------------------

#: A fixed, delimiter-free ASCII token written at a candidate path to probe the round trip. Pinned in the
#: artifact so a reader knows exactly what was set.
SENTINEL = "MFRT0"

#: A minimal MSH the fixtures share (its own MSH-9 = ADT^A01 — arbitrary; the probe never reads it).
_MSH = "MSH|^~\\&|A|B|C|D|20200101||ADT^A01|1|P|2.5.1"


def _fixture(segment: str) -> str:
    """A minimal message that CONTAINS ``segment`` (seeded with one empty field) so ``set()`` can extend
    it rather than raise ``KeyError`` on an absent segment."""
    return _MSH if segment == "MSH" else f"{_MSH}\r{segment}|"


def _path_verified(fixture: str, path: str) -> bool:
    """Whether ``path`` round-trips on BOTH parser backends: ``set(path, SENTINEL) -> encode() -> read
    back == SENTINEL`` on the built-in AND python-hl7 backends, and the two encodings agree byte-for-byte
    (a dormant parser-parity regression guard — NOT the round-trip proof, which is the read-back)."""
    from messagefoundry.parsing._backend import backend
    from messagefoundry.parsing.message import Message

    encodings: list[str] = []
    for builtin in (True, False):
        with backend(builtin=builtin):
            try:
                msg = Message.parse(fixture)
                msg.set(path, SENTINEL)
                encoded = msg.encode()
                read_back = Message.parse(encoded).field(path)
            except (KeyError, ValueError):
                return False  # padding limits / an unsettable path -> unverified (degrades to free-text)
        if read_back != SENTINEL:
            return False
        encodings.append(encoded)
    return encodings[0] == encodings[1]


def verified_paths() -> dict[str, dict[str, list[int] | list[str]]]:
    """Segment-keyed round-trip-verified paths for the picker's P3 badge — ``{seg: {"fields":[…],
    "components":["f.c",…]}}``. Verification is per-segment (independent of the enclosing structure), so a
    path proven once is proven for every structure. **Build-time** by design: the ``backend()`` switch
    flips a module-global read per-parse, so running this inside the async engine would corrupt concurrent
    parses — here it runs single-threaded (~<1 s over the whole schema). MSH-1/MSH-2 (the encoding-character
    fields) are structurally excluded — they are never value targets."""
    from messagefoundry.hl7schema import hl7_schema

    schema = hl7_schema()
    out: dict[str, dict[str, list[int] | list[str]]] = {}
    for seg, segdef in schema["segments"].items():
        fixture = _fixture(seg)
        fields: list[int] = []
        components: list[str] = []
        for field in segdef["fields"]:
            fi = field["index"]
            if seg == "MSH" and fi in (1, 2):
                continue  # MSH-1/MSH-2 are the encoding characters — never a value target
            if _path_verified(fixture, f"{seg}-{fi}"):
                fields.append(fi)
            for comp in field["components"]:
                if _path_verified(fixture, f"{seg}-{fi}.{comp['index']}"):
                    components.append(f"{fi}.{comp['index']}")
        out[seg] = {"fields": fields, "components": components}
    return out


def to_json() -> dict[str, object]:
    """The bundled ``ide/media/hl7structures.json`` payload (P2 ``triggerToStructure``/``structureSegments``
    + P3 ``verified``).

    Everything is sorted so the artifact is deterministic (the CI byte-equal sync test depends on it)."""
    trig = build_trigger_map()
    structures = sorted(set(trig.values()))
    return {
        "version": SUPPORTED_VERSION,
        "sentinel": SENTINEL,
        "triggerToStructure": dict(sorted(trig.items())),
        "structureSegments": {s: structure_segments(s) for s in structures},
        "verified": verified_paths(),
    }
